// Export functions for filtering and categorization
export function getUniqueCategories(): string[] {
  const categories = [...new Set(aiToolsDatabase.map((tool) => tool.category))]
  return categories.sort()
}

export function getUniqueRegions(): string[] {
  const regions = new Set<string>()

  aiToolsDatabase.forEach((tool) => {
    if (tool.region) {
      // Split regions by "/" and add each one
      tool.region.split("/").forEach((region) => {
        regions.add(region.trim())
      })
    }
  })

  // Add some popular regions that might not be in the data
  regions.add("USA")
  regions.add("UK")
  regions.add("Global")
  regions.add("Europe")
  regions.add("Canada")
  regions.add("Australia")

  return Array.from(regions).sort()
}

